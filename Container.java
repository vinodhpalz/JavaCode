class Container
{
	private int containerId;
	private int containerAge;
	public Container(int contId, int contAge)
	{
		containerId = contId;
		containerAge = contAge;
	}
	public int getContainerId()
	{
		return this.containerId;	
	}
	public int getContainerAge()
	{
		return containerAge;
	}
}